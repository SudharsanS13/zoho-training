class Comparision{
	public static void main(String[] args) {
	int num1 = 2434;
	int num2 = 2346;
	 if (num1 < num2) {
	 System.out.println ("Num1 is Less than Num2");
	 }
	 else if (num1 > num2) {
	System.out.println("Num1 is Greater than Num2");
	 }
	 else if (num1 == num2) {
	System.out.println("Num1 is Equal to Num2");
	}
 	else {
	System.out.println("!Invalid Number");
	}
}
	}
